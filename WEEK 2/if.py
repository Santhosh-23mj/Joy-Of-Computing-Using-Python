#!/usr/bin/python3

num = int(input("Enter a number. "))
if ( num == 1 ):
    print("You entered 'ONE'")
if ( num == 2 ):
    print("You entered 'TWO'")
if ( num == 3 ):
    print("You entered 'THREE'")
if ( num == 4 ):
    print("You entered 'FOUR'")
if ( num == 5 ):
    print("You entered 'FIVE'")
if ( num > 5 ):
    print("Please enter a number less than 5")

